!-----------------------------------------------------------------------
module v__in
  use c__eprm, only: nsp,nqa,lpn1
  implicit none

  real(kind=8),save ::  delt
  real(kind=8),save ::  q(nsp,nqa)
  real(kind=8),save ::  pi,tpi,hpi

  contains
!-----------------------------------------------------------------------
!-----------------------------------------------------------------------
  subroutine v__in__ini
!-----------------------------------------------------------------------
    pi = 4.d0 * atan (1.d0)
    tpi = pi*2.d0
    hpi = pi*5.d-1
!-----------------------------------------------------------------------
    delt  = 1.00d-1
!    delx  = 2.00d-1    ! DELX is defined in p__initmg
!-----------------------------------------------------------------------
!-----------------------------------------------------------------------
! 質量
    q(1,1) = 1.0d0
    q(2,1) = 1.0d0
!-----------------------------------------------------------------------
! 電荷(電子)
    q(1,2) = -1.0d0
    q(2,2) = -1.0d0
!-----------------------------------------------------------------------
  end subroutine v__in__ini
!-----------------------------------------------------------------------
!-----------------------------------------------------------------------
end module v__in
